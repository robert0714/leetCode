public class Solution {
	 

	public boolean isValid(String s) {
		Stack<Character> stack = new Stack<Character>();
		final char[] arrays = s.toCharArray();
		for (char c : arrays ) {
			if (c == '(')
				stack.push(')');
			else if (c == '{')
				stack.push('}');
			else if (c == '[')
				stack.push(']');
			else if (stack.isEmpty() || stack.pop() != c)
				return false;
		}
		return stack.isEmpty();
    }
}
